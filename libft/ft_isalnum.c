int	ft_isalnum(int c, int choice)
{
	if (choice == 1)
	{
		if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || \
		(c >= 'A' && c <= 'Z') || c == '?')
			return (1);
	}
	if (choice == 0)
	{
		if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || \
		(c >= 'A' && c <= 'Z'))
			return (1);
	}
	return (0);
}
